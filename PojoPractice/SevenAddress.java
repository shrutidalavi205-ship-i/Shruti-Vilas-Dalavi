package PojoPractice;

public class SevenAddress {
 
	String city;
	String state;
	
	
	public SevenAddress(String city, String state) {
		super();
		this.city = city;
		this.state = state;
	}
	
	void display() {
		System.out.println("City = " + city);
		System.out.println("State = " + state);
	}
	
	
}
